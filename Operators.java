package com.capg.day;

public class Operators {
  public static void main(String []arg) {
	  //add
	  int num1 = 10;
	  int num2 = 20;
	  int num3 = num1+num2;
	  System.out.println("Sum is "+num3);
	  //sub
	  int numa = 70;
	  int numb = 20;
	  int numc = numa-numb;
	  System.out.println("Sum is "+numc);
	  //mul
	  int numx = 30;
	  int numy = 20;
	  int numz = numx*numy;
	  System.out.println("Sum is "+numz);
	  //div
	  int num4 =500;
	  int num5 = 25;
	  int num6 = num4/num5;
	  System.out.println("Sum is "+num6);
	  
  }
}
